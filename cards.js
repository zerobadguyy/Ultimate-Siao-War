function normalizeCardText(value) {
  return repairText(value)
    .replace(/พิงค์เบอรี่/g, "พิงค์เบอร์รี่")
    .replace(/พิ้งค์เบอรี่/g, "พิ้งค์เบอร์รี่")
    .replace(/เบธานี่/g, "เบธานี")
    .replace(/เบลลานี(?!ย์)/g, "เบลลานีย์")
    .replace(/เบลานี/g, "เบลลานีย์");
}

